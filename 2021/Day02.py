def calculateDistance():
    depth = 0
    horizontal = 0
    for command in commands:
        match command:
            case [Command.forward, distance]:
                horizontal = horizontal + distance
            case [Command.down, distance]:
                depth = depth + distance
            case [Command.up, distance]:
                depth = depth - distance
    
    return depth * horizontal

def calculateDistanceWithAim():
    aim = 0
    depth = 0
    horizontal = 0
    for command in commands:
        match command:
            case [Command.forward, distance]:
                horizontal = horizontal + distance
                depth = depth + (aim * distance)
            case [Command.down, distance]:
                aim = aim + distance
            case [Command.up, distance]:
                aim = aim - distance
    
    return depth * horizontal

